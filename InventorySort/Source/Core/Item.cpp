#include "Item.hpp"
#include <sstream>

//////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////

Item::Item(std::string t_name, const EItemType t_type, const float t_price, const int t_quantity)
	: m_name(std::move(t_name)), m_type(t_type), m_price(t_price), m_quantity(t_quantity)
{
}

//////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////

void Item::setName(const std::string& t_name)
{
	m_name = t_name;
}

//////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////

void Item::setType(const EItemType t_type)
{
	m_type = t_type;
}

//////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////

void Item::setPrice(const float t_price)
{
	m_price = t_price;
}

//////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////

void Item::setQuantity(const int t_quantity)
{
	m_quantity = t_quantity;
}

//////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////

std::string Item::getName() const
{
	return m_name;
}

//////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////

EItemType Item::getType() const
{
	return m_type;
}

//////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////

float Item::getPrice() const
{
	return m_price;
}

//////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////

int Item::getQuantity() const
{
	return m_quantity;
}

//////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////

std::string Item::getMemoryAddress() const
{
	std::stringstream ss;
	ss << this;
	return ss.str();
}

